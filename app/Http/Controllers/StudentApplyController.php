     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'student_id' =>'required',
            'f_name' =>'required',
            'm_initial' =>'required',
            'l_name' =>'required',
            'dob' =>'required',
            'email' =>'required',
            'mobile' =>'required',
            // 'course_name' =>'required'
        ]);
        
        Student::create($request->all());

        // dd(Auth::user()->id);

        return redirect()->route('student-apply.index')->with('success', 'Student registered successfully');
    }

